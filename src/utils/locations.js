export const STATES = Object.keys(INDIA_LOCATIONS).sort()

export const getCitiesForState = (state) => {
  if (!state || state === 'Other') return []
  return INDIA_LOCATIONS[state] || []
}

export const getAllCities = () => {
  const all = []
  Object.values(INDIA_LOCATIONS).forEach(cities => all.push(...cities))
  return [...new Set(all)].sort()
}

export const getStateForCity = (city) => {
  for (const [state, cities] of Object.entries(INDIA_LOCATIONS)) {
    if (cities.includes(city)) return state
  }
  return null
}
